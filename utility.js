function boldItalicUnder(inputStyle) {
  const firstThreeStyle = document.getElementById(inputStyle);
  firstThreeStyle.style.color = 'white';
  firstThreeStyle.style.backgroundColor = 'black';
}

function inputAreaFunc(inputFieldParam) {
  const inputArea = document.getElementById(inputFieldParam);
  inputArea.style.fontWeight = 'bold';
}
